package com.raafstudio.goahead.people.model;

public class SubBrand {
	String subBrandID;
	String subBrandName;
	String subBrandCode;
	String brandName;
	String brandCode;

	public String getSubBrandID() {
		return subBrandID;
	}

	public void setSubBrandID(String subBrandID) {
		this.subBrandID = subBrandID;
	}

	public String getSubBrandName() {
		return subBrandName;
	}

	public void setSubBrandName(String subBrandName) {
		this.subBrandName = subBrandName;
	}

	public String getSubBrandCode() {
		return subBrandCode;
	}

	public void setSubBrandCode(String subBrandCode) {
		this.subBrandCode = subBrandCode;
	}

	public String getBrandName() {
		return brandName;
	}

	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}

	public String getBrandCode() {
		return brandCode;
	}

	public void setBrandCode(String brandCode) {
		this.brandCode = brandCode;
	}

}
